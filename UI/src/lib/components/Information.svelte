<header class="info">
  <h4><i class="fa fa-list" />Requirements</h4>
  <li>
    Movie name should be in the format <mark class="alert">Name (Year)</mark>
  </li>
  <li>
    Initially, internet connection is required for retrieving movie details
  </li>

  <h4><i class="fa fa-list" />Usage</h4>
  <li>
    Choose movie folder by clicking the <mark class="alert"
      ><i class="fa fa-folder-open" /></mark
    > button in the toolbar
  </li>
  <li>
    Wait till details for all the movies in the selected folder is retreived
  </li>
  <li>
    Once the details are fetched, movies will be listed on the left sidebar
  </li>
  <li>Click any movie to see its details</li>
  <li>
    Use the <mark class="alert"><i class="fa fa-filter" /></mark> to filter movies
    by genre
  </li>
  <li>
    To search movies by name/year, use the <mark class="alert"
      ><i class="fa fa-search" /></mark
    >
  </li>
  <li>
    Movies, for which details were unavailable will be in <mark class="alert"
      >red color</mark
    >
  </li>
  <li>Click the movie to add its details manually</li>
  <li>
    Use the <mark class="alert"><i class="fa fa-arrow-circle-down" /></mark> button
    in toolbar to import data from MovieSieve(.ms) file
  </li>
  <li>
    Use the <mark class="alert"><i class="fa fa-arrow-circle-up" /></mark> button
    in toolbar to export data into MovieSieve(.ms) file
  </li>
  <li>
    Please <mark class="alert"><i class="fa fa-save" /></mark> before exiting. Else,
    latest additions wont be available, next time you open the app
  </li>
</header>
